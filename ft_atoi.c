#include "libft.h"

int	ft_atoi(const char *str)
{
	int	n;
	int	m;
	int	c;

	c = 0;
	n = 0;
	m = 1;
	while (str[c] == 32 || str[c] == '\f' || str[c] == '\n' || str[c] == '\r'
		|| str[c] == '\t' || str[c] == '\v')
		c++;
	if (str[c] == '-' || str[c] == '+')
	{
		if (str[c] == '-')
		{
			m = -1;
		}
		c++;
	}
	while (str[c] >= 48 && str[c] <= 57)
	{
		n = n * 10 + (str[c++] - '0');
	}
	return (m * n);
}
